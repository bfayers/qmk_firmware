// Tap Dance Declarations
enum {
     TD_LSHIFT_CAPS,
};

// Tap Dance Definitions
tap_dance_action_t tap_dance_actions[] = {
    // Tap once for Escape, twice for Caps Lock
    [TD_LSHIFT_CAPS] = ACTION_TAP_DANCE_DOUBLE(KC_LSFT, KC_CAPS),
};

// Macro Definitions
enum custom_keycodes {
    SSHOT = SAFE_RANGE,
    CMD_SPC,
    TEST_MACRO,
};

// This will be true when the switch is set to windows, and false when the switch is set to mac.
bool win_mac = true;

bool dip_switch_update_user(uint8_t index, bool active) {
    if (index == 0) {
        win_mac = active;
    }
    return true;
}

// Macro Processing
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
          case SSHOT:
               if (record->event.pressed) {
                    if (win_mac) {
                         SEND_STRING(SS_LGUI(SS_LSFT("S")));
                    } else {
                         SEND_STRING(SS_LCMD(SS_LOPT("8")));
                    }
               }
               break;
          case CMD_SPC:
               if (record->event.pressed) {
                    SEND_STRING(SS_LCMD(" "));
               }
               break;
          case TEST_MACRO:
               if (record->event.pressed) {
                    if (win_mac) {
                         SEND_STRING("Hello World! WIN");
                    } else {
                         SEND_STRING("Hello World! MAC");
                    }
               }
               break;
    }
    return true;
}

